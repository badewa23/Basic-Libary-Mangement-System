    private void jBDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDeleteActionPerformed
        // TODO add your handling code here:
        if (jTableUser.getSelectionModel().isSelectionEmpty()){
            JOptionPane.showMessageDialog(null, "You have not selected any thing" );
        }
        else{
            DefaultTableModel model = (DefaultTableModel) jTableUser.getModel();
            String userID = model.getValueAt(jTableUser.getSelectedRow(), 0).toString();
            String dialog = Manage.deleteUser(userID);
            if(dialog == null)
                updateList();
             else
                JOptionPane.showMessageDialog(null, dialog);
        }

    }//GEN-LAST:event_jBDeleteActionPerformed

    private void jBAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAddActionPerformed
        String userName = jtxtUserName.getText();
        String passWord = jtxtPassWord.getText();
        String roles = (String) jCBRole.getSelectedItem();
        String dialog = Manage.newUser(userName, passWord, roles);
        if(dialog== null)
            updateList();
        else
        JOptionPane.showMessageDialog(null, dialog);
    }//GEN-LAST:event_jBAddActionPerformed

