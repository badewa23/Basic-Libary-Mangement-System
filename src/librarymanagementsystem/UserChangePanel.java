    public final void updateList(){
        ResultSet rst  = Manage.createTableUser();
        DefaultTableModel model = (DefaultTableModel) jTableUser.getModel();
        model.setRowCount(0);
        try {
            while(rst.next()){
                model.addRow(new String[] {rst.getString(1), rst.getString(2), 
                    rst.getString(3), rst.getString(4)});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Table Code did not work in UpdateList");
        }
    }

